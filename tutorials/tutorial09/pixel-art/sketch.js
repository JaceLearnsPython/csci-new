function drawRow (row, topX, topY, pixelWidth) {

    let colorOptions = ['transparent','hotpink', 'black', 'white']

    for (let i = 0; i < row.length; i++) {
        const color = colorOptions[row[i]];
        fill(color);
        square(topX, topY, pixelWidth)
        topX += pixelWidth;
    }
}

function drawPixelArt(grid, topX, topY, pixelWidth) {
    for (let i = 0; i < grid.length; i++) {

        drawRow(grid[i], topX, topY, pixelWidth);
        topY += pixelWidth; // Why did you choose this value?
    }
}